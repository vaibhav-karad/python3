# ',' Comma variable name.
name = 'Sars_virus_19'
disease = 'Covid-19'

print("\nThe name of virus is", name)
print("the name of virus is {} " .format(disease))

print("The name of virus is {} and it causes {}".format(name, disease))

# Using 'f' fucntion in print statement.
print(f"The name of virus is {name} and it causes {disease}")

# Used Concatination
print("The name of virus is" + " " + name)
