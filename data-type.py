# List / Collection of  multi  datatype, enclosed in square brackets.

#Numbers
str1 = 'alpha'
num1 = 1221
first_list = [str1, "DevOps", 33, num1, 1.2]

#Printing a List

print(first_list)

# Tuple / Collections of multi datatype, enclosed in round brackets.
first_tuple = (str1, "DevOps", 33, num1, 1.2)

# printing a Tuple
print(first_tuple)

# Dictionary / elements in the dictionary are always in pairs (key:value), curly brackets.
first_dic = {"name":"Vaibhav", "Weight":65, "Exercises":["running", "pushup", "legs"]}
print(first_dic)

